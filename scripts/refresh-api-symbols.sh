#!/bin/bash

# Regenerates scripts/api-symbols.txt — the ground-truth manifest of Apple API
# members that check-api-symbols.sh polices skills against.
#
# WHY A MANIFEST INSTEAD OF READING THE SDK IN CI:
# CI runs on ubuntu-latest, which has no Xcode and no Apple SDK. So a maintainer
# runs this on a Mac, commits the result, and CI checks the docs against the
# committed file. Same shape as versions.env: ground truth is refreshed by hand
# on Apple's cadence, and enforced automatically on every PR.
#
# WHAT IT EXTRACTS, from the SDK's .swiftinterface files (authoritative — these
# are the real declarations, not documentation about them):
#   - every member (case / static var / static func) of each POLICED type
#   - whether a type or member is marked @available(..., deprecated: ...)
#
# WHEN TO RUN IT: after installing a new Xcode. A new SDK is exactly when a
# symbol we teach can vanish or become deprecated — that's the whole point.
#
#   ./scripts/refresh-api-symbols.sh      # rewrite the manifest
#   git diff scripts/api-symbols.txt      # review what Apple changed
#
# HONEST LIMITATION: the manifest can only describe the SDK you have installed.
# Symbols introduced in a newer SDK are simply absent, so they go unpoliced
# rather than being falsely flagged. check-api-symbols.sh skips any type it has
# no ground truth for — silence here means "unknown", not "verified".

set -uo pipefail
REPO_DIR="$(cd "$(dirname "$0")/.." && pwd)"
cd "$REPO_DIR" || exit 1

MANIFEST="scripts/api-symbols.txt"

# Types to police, as "Framework:Type". Keep this list TIGHT and deliberate.
#
# Good candidates: closed enums whose cases we enumerate in skills (catch
# clauses, migration tables). A wrong case name there is a compile error we'd be
# teaching — the exact bug this exists to catch.
#
# Bad candidates: big open structs like SystemLanguageModel, whose members
# arrive via extensions across files. Extraction would miss some and the check
# would flag correct code. Don't add a type unless its members are all declared
# in one block.
POLICED="
FoundationModels:GenerationError
FoundationModels:SamplingMode
"

if ! command -v xcrun >/dev/null 2>&1; then
    echo "✗ xcrun not found — this script needs macOS with Xcode installed."
    echo "  (CI does not run it; it reads the committed $MANIFEST instead.)"
    exit 1
fi

XCODE_VER=$(xcodebuild -version 2>/dev/null | tr '\n' ' ' | sed 's/  */ /g')
PLATFORMS_DIR="$(xcode-select -p)/Platforms"

# Find the richest .swiftinterface for a framework (any platform/arch will do —
# member lists don't vary by arch).
find_interface() {
    find "$PLATFORMS_DIR" -name "*.swiftinterface" -path "*/$1.framework/*" 2>/dev/null \
        | sort | head -1
}

# Print "member" lines for a type, plus a DEPRECATED marker when the declaration
# carries @available(..., deprecated: ...). Walks brace depth from the type's
# declaration to its closing brace so nested types don't leak in.
extract_type() {
    awk -v type="$2" '
      # Buffer @available lines so a deprecation attaches to the decl that follows.
      /^[ \t]*@available/ { avail = avail $0; next }

      # The declaration line. Its own "{" must be counted, or depth starts at 0
      # and the first member immediately trips the depth<=0 exit below.
      !found && $0 ~ ("(enum|struct|class) " type "[ :{]") {
          found = 1
          depth = gsub(/\{/, "{") - gsub(/\}/, "}")
          print "TYPE\t" (avail ~ /deprecated/ ? "DEPRECATED" : "OK")
          avail = ""
          next
      }

      found {
          name = ""
          if (match($0, /^[ \t]*case [a-zA-Z_][a-zA-Z0-9_]*/)) {
              name = substr($0, RSTART, RLENGTH); sub(/^[ \t]*case /, "", name)
          } else if (match($0, /public static (var|func) [a-zA-Z_][a-zA-Z0-9_]*/)) {
              name = substr($0, RSTART, RLENGTH); sub(/.*(var|func) /, "", name)
          }
          if (name != "") print name "\t" (avail ~ /deprecated/ ? "DEPRECATED" : "OK")
          avail = ""

          # Members with bodies ({ get } etc.) nest — track depth so we stop at
          # the type`s closing brace and never bleed into the next declaration.
          depth += gsub(/\{/, "{") - gsub(/\}/, "}")
          if (depth <= 0) exit
      }

      { avail = "" }   # a non-@available line clears any buffered attribute
    ' "$1"
}

{
    echo "# GENERATED by scripts/refresh-api-symbols.sh — DO NOT EDIT BY HAND."
    echo "# Ground truth for check-api-symbols.sh. Refresh after installing a new Xcode."
    echo "#"
    echo "# SDK: $XCODE_VER"
    echo "#"
    echo "# Format:  <Type>\\t<member>\\t<OK|DEPRECATED>"
    echo "# A type with no rows here is UNPOLICED — absence means unknown, not verified."
    echo ""
} > "$MANIFEST"

total=0
for entry in $POLICED; do
    framework="${entry%%:*}"
    type="${entry##*:}"

    iface=$(find_interface "$framework")
    if [ -z "$iface" ]; then
        echo "  ⚠ $framework: no .swiftinterface found in the installed SDK — skipping $type"
        continue
    fi

    rows=$(extract_type "$iface" "$type" | sort -u)
    if [ -z "$rows" ]; then
        echo "  ⚠ $framework.$type: declared in POLICED but not found in the SDK — is it renamed or removed?"
        continue
    fi

    count=0
    while IFS=$'\t' read -r member status; do
        [ -z "$member" ] && continue
        printf '%s\t%s\t%s\n' "$type" "$member" "$status" >> "$MANIFEST"
        [ "$member" != "TYPE" ] && count=$((count + 1))
    done <<< "$rows"

    echo "  ✓ $framework.$type — $count members"
    total=$((total + count))
done

echo ""
echo "✓ Wrote $MANIFEST ($total members across $(grep -c . <<< "$POLICED") types)"
echo "  Review with: git diff $MANIFEST"
